import spidev

spi = spidev.SpiDev()
spi.open(1, 0)
spi.max_speed_hz = 1000000

ledstart = [0x00, 0x00, 0x00, 0x00]
ledstop = [0xff, 0xff, 0xff, 0xff]
led1 = [0xff, 0xff, 0xff, 0xff]
led2 = [0xff, 0xff, 0x00, 0x00]
led3 = [0xff, 0x00, 0xff, 0x00]
led4 = [0xff, 0x00, 0x00, 0xff]
led5 = [0xf0, 0xff, 0xff, 0xff]

to_send = ledstart + led1 + led2 + led3 + led4 + led5 + ledstop

spi.xfer(to_send)
